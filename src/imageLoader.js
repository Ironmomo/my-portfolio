import axenitaImagePath from './assets/axenita.png'
document.addEventListener('DOMContentLoaded', () => {
    const container = document.querySelector('.image-container.axenita');
    if (container) {
        const img = document.createElement('img');
        img.src = axenitaImagePath;
        img.alt = 'axenita';
        container.appendChild(img);
    } else {
        console.error('Container not found!');
    }
});

